residents = []
lines = data.strip().split('\n')

for line in lines:
    if not line.strip():
        continue
    
    # Parse format: 201/2\tName
    parts = line.split('\t')
    if len(parts) < 2:
        continue
    
    location = parts[0].strip()
    name = parts[1].strip()
    
    if not name or not location:
        continue
    
    # Parse location: 201/2
    match = re.match(r'(\d)(\d{2})/(\d)', location)
    if not match:
        continue
    
    floor = int(match.group(1))
    block = int(match.group(2))
    room_num = int(match.group(3))
    
    # Map room number to type
    if room_num == 2:
        room_type = 'small'
    elif room_num in [3, 4]:
        room_type = 'large'
    else:
        continue
    
    residents.append({
        'full_name': name,
        'floor': floor,
        'block': block,
        'room_type': room_type,
        'is_block_leader': False  # Will be set manually later
    })

print(f"Total residents parsed: {len(residents)}")
for r in residents[:5]:
    print(f"  {r['full_name']} - Floor {r['floor']}, Block {r['block']}, Room {r['room_type']}")
